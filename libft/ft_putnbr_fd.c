#include <unistd.h>

void	ft_putnbr_fd(int n, int fd)
{
	char	m;

	if (n == -2147483648)
		write(fd, "-2147483648", 11);
	else if (n < 0)
	{
		write(fd, "-", 1);
		n *= -1;
	}
	if (n < 10 && n >= 0)
	{
		m = n + '0';
		write(fd, &m, 1);
	}
	else if (n > 0)
	{
		ft_putnbr_fd(n / 10, fd);
		m = n % 10 + '0';
		write(fd, &m, 1);
	}
}
